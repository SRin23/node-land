//Comparison 비교연산자
console.log(1==1)   //1과 1이 같은가? - true
console.log(1==2)   //1과 2가 같은가 - false

console.log(1>2)    //1이 2보다 큰가? - false
console.log(1<2)    //1이 2보다 작은가? - true

console.log(1===1)  //1과 1이 정확히 같은가 - true
console.log(1===2)  //1과 2가 정확히 같은가 - false


// = 는 대입연산자
// == 는 비교연산자로 다른 의미를 가진다.